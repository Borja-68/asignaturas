    private void getOperacion(){
         switch(operacion){
            case '+':ventana.resultado+=Integer.parseInt(ventana.segundoNumero);break;
            case '-':ventana.resultado-=Integer.parseInt(ventana.segundoNumero);break;
            case '*':ventana.resultado*=Integer.parseInt(ventana.segundoNumero);break;
            case '/':ventana.resultado/=Integer.parseInt(ventana.segundoNumero);break;
            case '^':ventana.resultado=(int)Math.pow(ventana.resultado,Integer.parseInt(ventana.segundoNumero)); break;
            default:break;
        }
       
    }
