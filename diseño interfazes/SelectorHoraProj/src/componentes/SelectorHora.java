    public SelectorHora() {
        initComponents();
        notificador=new PropertyChangeSupport(this);
        configurarListenersDeUsuario();
    }
    
 private void configurarListenersDeUsuario() {
    // Cuando el usuario cambia el spinner de hora
       spHora.addChangeListener(e -> {
       int horaSeleccionada = (int) spHora.getValue();
       setHora(horaSeleccionada);
       });
       // Cuando el usuario cambia el spinner de minutos
       spMinutos.addChangeListener(e -> {
       int minutoSeleccionado = (int) spMinutos.getValue();
       setMinutos(minutoSeleccionado);
       });
    }
 
        // Para suscribirse a TODOS los cambios
    public void agregarOyente(PropertyChangeListener oyente) {
     notificador.addPropertyChangeListener(oyente);
    }
    // Para suscribirse a los cambios de una propiedad concreta
    public void agregarOyentePropiedad(String propiedad,PropertyChangeListener oyente) {
     notificador.addPropertyChangeListener(propiedad, oyente);
    }
    // Para darse de baja de la suscripci√≥n
    public void eliminarOyente(PropertyChangeListener oyente) {
     notificador.removePropertyChangeListener(oyente);
    }
